#include "../includes/ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (write(1, "(null)", 6));
	while (*s)
	{
		ft_putchar(*s);
		s++;
		i++;
	}
	return (i);
}
